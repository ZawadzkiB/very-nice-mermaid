#!/usr/bin/env node
/**
 * Regenerate the `examples/` gallery: render every source diagram in the full
 * style × theme matrix and rewrite examples/README.md. Run with `npm run examples`
 * (build first — it drives the built CLI, `dist/cli/index.js`). Deterministic, so
 * re-running only changes an image when the renderer changed.
 */
import { execFileSync } from "node:child_process";
import { readdirSync, mkdirSync, writeFileSync } from "node:fs";
import { fileURLToPath } from "node:url";
import { dirname, join, basename } from "node:path";

const here = dirname(fileURLToPath(import.meta.url));
const repo = join(here, "..");
const cli = join(repo, "dist", "cli", "index.js");
const srcDir = join(repo, "examples", "src");
const pngDir = join(repo, "examples", "png");
const svgDir = join(repo, "examples", "svg");
mkdirSync(pngDir, { recursive: true });
mkdirSync(svgDir, { recursive: true });

const STYLES = ["clean", "sketch"];
const THEMES = ["light", "dark"];
const sources = readdirSync(srcDir)
  .filter((f) => f.endsWith(".mmd"))
  .map((f) => ({ name: basename(f, ".mmd"), file: join(srcDir, f) }))
  .sort((a, b) => a.name.localeCompare(b.name));

const render = (file, style, theme, fmt, out) =>
  execFileSync(
    "node",
    [cli, "render", file, "--style", style, "--theme", theme, "-f", fmt, "-o", out, ...(fmt === "png" ? ["--scale", "2"] : [])],
    { stdio: ["ignore", "ignore", "pipe"] },
  );

let count = 0;
const failures = [];
for (const s of sources) {
  for (const style of STYLES) {
    for (const theme of THEMES) {
      const key = `${s.name}-${style}-${theme}`;
      try {
        render(s.file, style, theme, "png", join(pngDir, `${key}.png`));
        render(s.file, style, theme, "svg", join(svgDir, `${key}.svg`));
        count++;
      } catch (err) {
        failures.push(`${key}: ${String(err.stderr || err.message).split("\n")[0]}`);
      }
    }
  }
}

// --- gallery README: one section per diagram, clean | sketch columns, light+dark rows ---
const title = (s) => s.charAt(0).toUpperCase() + s.slice(1);
let md = `# Examples\n\n> Auto-generated by \`npm run examples\` — **do not edit by hand.**\n> Every diagram type rendered in each \`--style\` (clean / sketch) and theme (light / dark).\n> Regenerated on each run so the gallery always matches the current renderer.\n\n`;
for (const s of sources) {
  md += `## ${title(s.name)}\n\n`;
  md += `\`\`\`mermaid\n${execFileSync("cat", [s.file], { encoding: "utf8" }).trim()}\n\`\`\`\n\n`;
  md += `| | Clean | Sketch |\n|---|---|---|\n`;
  for (const theme of THEMES) {
    md += `| **${title(theme)}** | ![${s.name} clean ${theme}](./png/${s.name}-clean-${theme}.png) | ![${s.name} sketch ${theme}](./png/${s.name}-sketch-${theme}.png) |\n`;
  }
  md += `\n`;
}
writeFileSync(join(repo, "examples", "README.md"), md, "utf8");

console.log(`examples: rendered ${count} × (png+svg) across ${sources.length} diagrams, ${STYLES.length} styles, ${THEMES.length} themes`);
if (failures.length) {
  console.error(`examples: ${failures.length} render(s) failed:\n  ${failures.join("\n  ")}`);
  process.exit(1);
}
